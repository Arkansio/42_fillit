#include "fillit.h"

int			ft_load_tetmap(char *str, int map[4][4])
{
	int     i;
	int     j;

	i = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			if (str[i * 5 + j] == '#')
				map[i][j] = 1;
			else if (str[i * 5 + j] == '.')
				map[i][j] = 0;
			else
				return (-1);
			j++;
		}
		i++;
	}
	return (1);
}
